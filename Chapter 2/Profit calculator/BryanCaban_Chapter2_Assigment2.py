#Projected sales
#Calculator that shows the profit made from the total amount of sales


#the constant of the annual profit
ANNUALPROFIT = .23

#ask for the projected amount of total sales
totalsales = input('Projected amount of total sales: ') 

#Calculate the total profit
totalprofit = float(totalsales) * ANNUALPROFIT 

print(f'The Total profit is: ${totalprofit}')

